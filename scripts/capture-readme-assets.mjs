await run("npm", ["run", "proof:example"]);
await copyFile(proofScreenshot, readmeScreenshot);

if (!hasCommand("ffmpeg")) {
  console.log("ffmpeg not found; wrote screenshot only.");
  process.exit(0);
}

const browser = await chromium.launch();
